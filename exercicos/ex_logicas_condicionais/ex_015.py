n = int(input('Digite um valor entre 1 e 7 que seja inteiro:\n'))

if n == 1:
    print('Domingo')
elif n == 2:
    print('Segunda')
elif n == 3:
    print('Terça')
elif n == 4:
    print('Quarta')
elif n == 5:
    print('Quinta')
elif n == 6:
    print('Sexta')
elif n == 7:
    print('Sábado')
else:
    print('[ERRO] - Numero Inválido')
